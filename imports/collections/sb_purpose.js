import {Mongo} from "meteor/mongo";

export const Sb_Purpose = new Mongo.Collection("sb_purpose");
Sb_Purpose.schema = new SimpleSchema({
    name: {
        type: String,
        label: "Name"
    },
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
Sb_Purpose.attachSchema(Sb_Purpose.schema);


